        private void displayExistingBillingData() {
        try {
            try (Connection connection = DatabaseConnection.getConnection();) {
                String sql = "SELECT customerID, package, S_date, S_time, total FROM package";
                
                PreparedStatement preparedStatement = connection.prepareStatement(sql);
                ResultSet resultSet = preparedStatement.executeQuery();
                
                DefaultTableModel tablemodel = (DefaultTableModel) tab_billing.getModel();
                
                while (resultSet.next()) {
                String Fullname = resultSet.getString("customerID");
                String Package = resultSet.getString("PackageID");
                Date Date = resultSet.getDate("S_date");
                String Time = resultSet.getString("total");
               
                tablemodel.addRow(new Object[]{Fullname, Package, Date, Time});
                }
            }
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
        }
    }
    
