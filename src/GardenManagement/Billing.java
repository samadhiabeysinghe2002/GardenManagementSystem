        private void saveCustomerToDatabase() {
        try{
           
            String cusID = (String)cusCom.getSelectedItem();
            String packageid = (String)cb_pacid.getSelectedItem();
            Date selectedDate = datechooser.getDate();
            String total = txt_price.getText();
          
            
            if (cusID == null || cusID.trim().isEmpty()) {
            throw new IllegalArgumentException("Please enter customer ID.");
            }
           
   
            if (selectedDate == null) {
                throw new IllegalArgumentException("Please select a date.");
            }
            
            if (total == null) {
                throw new IllegalArgumentException("Please select a total.");
            }
          
            
            DefaultTableModel tableModel = (DefaultTableModel) tab_billing.getModel();
            tableModel.addRow(new Object[]{cusID,  packageid, selectedDate, total});
            
                try (Connection connection = DatabaseConnection.getConnection();){
                    String sql = "INSERT INTO customer (customer_id, full_name, package, date, time, am_pm, telephone, location, email_address) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)";
                    PreparedStatement preparedStatement = connection.prepareStatement(sql);
                    preparedStatement.setString(1, cusID);
                    preparedStatement.setString(2, packageid);
                    preparedStatement.setDate(3, new java.sql.Date(selectedDate.getTime()));
                    preparedStatement.setString(4, total);
           
                    
                    int rowsAffected = preparedStatement.executeUpdate();
                            
                    if (rowsAffected > 0) {
                        JOptionPane.showMessageDialog(this, "Student added successfully.");
                       
                        cusCom.setSelectedItem("");
                        datechooser.setDate(null);
                        txt_price.setText("");
                        cb_pacid.setSelectedItem("");
                       
                        
                    }
                    else {
                        JOptionPane.showMessageDialog(this, "Failed to add student.");
                    }
                    
            
        }}
            catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
            }
            catch (IllegalArgumentException e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
            catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "An unexpected error occurred during the save.");
            }
    }
    
