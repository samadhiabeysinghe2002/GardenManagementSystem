    private void Package_DeleteBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Package_DeleteBTNActionPerformed
        try {
            int selectedRow = tab_packages.getSelectedRow();
            if (selectedRow == -1) {
                throw new IllegalArgumentException("Please select a row to delete.");
            }
            int option = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete the selected row?", "Confirmation", JOptionPane.YES_NO_OPTION);
            if (option != JOptionPane.YES_OPTION) {
                return;
            }
            DefaultTableModel model = (DefaultTableModel) tab_packages.getModel();
            String pacId = (String) model.getValueAt(selectedRow, 0);
            model.removeRow(selectedRow);
            try (Connection connection = DatabaseConnection.getConnection(); )  {
                String sql = "DELETE FROM Packages WHERE package_id = ?";
                
                PreparedStatement preparedStatement = connection.prepareStatement(sql);
                preparedStatement.setString(1, pacId);
                
                int rowsAffected = preparedStatement.executeUpdate();
                
                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(this, "student details deleted successfully.");
                }
                else {
                    JOptionPane.showMessageDialog(this, "Failed to delete student details from the database.");
                }
            }
        }
        catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
        }
        catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "An unexpected error occurred during the deletion.");
        }
    }//GEN-LAST:event_Package_DeleteBTNActionPerformed

