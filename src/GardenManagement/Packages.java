        private void displayExistingPackageData() {
        try {
            try (Connection connection = DatabaseConnection.getConnection();) {
                String sql = "SELECT packageid, package_name, services, price FROM Packages";
                
                PreparedStatement preparedStatement = connection.prepareStatement(sql);
                ResultSet resultSet = preparedStatement.executeQuery();
                
                DefaultTableModel model = (DefaultTableModel) tab_packages.getModel();
                
                while (resultSet.next()) {
                String PackageID = resultSet.getString("packageid");
                String Packagename = resultSet.getString("package_name");
                String Services = resultSet.getString("services");
                String Price = resultSet.getString("price");
               

                
                model.addRow(new Object[]{PackageID, Packagename, Services, Price});
                }
            }
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
        }
    }
    
