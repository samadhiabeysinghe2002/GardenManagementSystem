        private void savePackageToDatabase() {
        try{
        String pacId = txt_pacid.getText();
        String pacName = txt_pacname.getText();
        String services = txt_services.getText();
        String price = txt_price.getText();
        
        if (pacId == null || pacId.trim().isEmpty()) {
            throw new IllegalArgumentException("Please enter Package Id.");
        }
        if (pacName == null || pacName.trim().isEmpty()) {
            throw new IllegalArgumentException("Please enter Package name.");
        }
        if (services == null || services.trim().isEmpty()) {
            throw new IllegalArgumentException("Please enter Services.");
        }
        if (price == null || price.trim().isEmpty()) {
            throw new IllegalArgumentException("Please enter Price.");
        }
        DefaultTableModel model = (DefaultTableModel) tab_packages.getModel();
        model.addRow(new Object[]{pacId, pacName, services, price});
        try (Connection connection = DatabaseConnection.getConnection();){
            String sql = "INSERT INTO Packages (package_id, packageid, package_name, services, price, gardeners) VALUES (?, ?, ?, ?, ?, ?)";
                    PreparedStatement preparedStatement = connection.prepareStatement(sql);
                    preparedStatement.setString(1, pacId);
                    preparedStatement.setString(2, pacName);
                    preparedStatement.setString(3, services);
                    preparedStatement.setString(4, price);
                    
                    int rowsAffected = preparedStatement.executeUpdate();
                    
                    if (rowsAffected > 0) {
                        JOptionPane.showMessageDialog(this, "Student added successfully.");
                        txt_pacid.setText(""); 
                        txt_pacname.setText("");
                        txt_services.setText("");
                        txt_price.setText("");

                        
                    }
                    else {
                        JOptionPane.showMessageDialog(this, "Failed to add student.");
                    }
        }

            }
            catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
            }
            catch (IllegalArgumentException e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
            catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "An unexpected error occurred during the save.");
            }
        }
    
